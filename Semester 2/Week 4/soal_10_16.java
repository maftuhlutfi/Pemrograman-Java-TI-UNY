import java.math.*;

public class soal_10_16 {
	public static void main(String[] args) {
		
		BigInteger i = (new BigInteger("10").pow(49));
		int total = 0;
		
		while (i.compareTo((new BigInteger("10").pow(50))) < 0) {
			// Cek bisa dibagi 2 atau 3
			if (i.remainder(new BigInteger("2")).equals(new BigInteger("0")) || i.remainder(new BigInteger("3")).equals(new BigInteger("0"))) {
				System.out.println(i.toString());
				++total;
			}

			// Jika sudah 10, break
			if (total == 10) {
				break;
			}

			// Increment i
			i = i.add(BigInteger.ONE);
		}
	}
}

